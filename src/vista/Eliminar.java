    private void botonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEliminarActionPerformed
        int idLibro = Integer.parseInt(textFieldIdLibro.getText());
        Registro reg = new Registro();
        boolean eliminado = reg.eliminar(idLibro);
        if(eliminado){
            JOptionPane.showMessageDialog(null, "El Libro ha sido eliminado");
        }else{
            JOptionPane.showMessageDialog(null, "No se ha encontrado un libro para eliminar");
        }
    }//GEN-LAST:event_botonEliminarActionPerformed

